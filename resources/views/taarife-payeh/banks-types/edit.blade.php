@push('js')
    <script>
        $(document).on('click', '.edit_banks_types', function(e) {
            e.preventDefault();
            var banks_types_id = $(this).val();
            // console.log(banks_types_id);

            $.ajax({
                type: "GET",
                url: "/banks-types/" + banks_types_id + "/edit",
                success: function(response) {
                    // console.log(response);
                    if (response.status == 404) {
                        Swal.fire({
                            icon: 'error',
                            title: 'خطا',
                            text: 'متأسفانه خطایی رخ داده است، لطفاً چند لحظه دیگر امتحان کنید',
                        })
                    } else {
                        $("#editInfo").modal("show");

                        $("#edit_banks_types_id").val(banks_types_id);
                        $("#edit_bank_code").val(response.banks_types.bank_code);
                        $("#edit_bank_name").val(response.banks_types.bank_name);
                    }
                },
            });
        });

        $(document).on("click", ".updateBanksTypes", function(e) {
            e.preventDefault();
            var banks_types_id = $("#edit_banks_types_id").val();
            var data = {
                banks_types_id: $("#edit_banks_types_id").val(),
                bank_code: $("#edit_bank_code").val(),
                bank_name: $("#edit_bank_name").val(),
            };

            $.ajaxSetup({
                headers: {
                    "X-CSRF-TOKEN": $('meta[name="csrf-token"]').attr("content"),
                },
            });

            $.ajax({
                type: "PUT",
                url: "/banks-types/" + banks_types_id,
                data: data,
                dataType: "json",
                success: function(response) {
                    // console.log(response);
                    Swal.fire(
                            response.message,
                            response.status,
                            'success'
                        )
                        .then((result) => {
                            $("#editInfo").modal("hide");
                            $("#editInfo").find("input").val("");
                            edit_clearErrors();
                            fetchData();
                        });
                },
                error: function(errors) {
                    edit_clearErrors();
                    $.each(errors.responseJSON.errors, function(key, err_values) {
                        // console.log(key);
                        // console.log(err_values);
                        $("#edit_" + key + "_error").text(err_values[0]);
                        $("#edit_" + key).addClass("is-invalid");
                    });
                }
            });
        });

        //call function on modal shown
        $('#editInfo').on('shown.bs.modal', function(e) {
            // alert("hello");
            $(".sidebar-mini").removeClass("sidebar-open");
            $(".sidebar-mini").addClass("sidebar-collapse");
        });

        //call function on hiding modal
        $('#editInfo').on('hidden.bs.modal', function(e) {
            // alert("bye");
            edit_clearErrors();
        })

        function edit_clearErrors() {
            $("#edit_bank_code_error").text("");
            $("#edit_bank_code").removeClass("is-invalid");
            $("#edit_bank_name_error").text("");
            $("#edit_bank_name").removeClass("is-invalid");
        }
    </script>
@endpush
